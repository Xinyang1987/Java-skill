package oneDimension;

/**
 * 数组
 */
public class demo1 {
    public static void main(String[] args) {
        int[] arr = {1, 2};
        int[] brr = {3, 4};
        int[] crr = new int[]{5, 6};

        for (int j : arr) {
            System.out.println(j);
        }

        for (int j : brr) {
            System.out.println(j);
        }

        for (int j : crr) {
            System.out.println(j);
        }
    }
}
